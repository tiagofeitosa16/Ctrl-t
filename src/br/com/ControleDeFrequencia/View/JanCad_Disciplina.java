    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
        // TODO add your handling code here:
        if (!(jTFDisciplina.getText().isEmpty()) && !(jTFCargaHoraria.getText().isEmpty()) &&
           (jRBMedio.isSelected() || jRBSuperior.isSelected()) && (jCBProfessor.getSelectedItem() != "") &&
           ((jCBSemestre.getSelectedItem() != "Selecione..." ) || (jCBSerie.getSelectedItem() != "Selecione..." ))){
            
            this.infoJanela = new HashMap<>();
            
            this.infoJanela.put("Disciplina", jTFDisciplina.getText());
            
            infoJanela.put("Semestre", "");
            
            if (jRBMedio.isSelected()){
                infoJanela.put("Serie", String.valueOf(jCBSerie.getSelectedIndex()));
            }else if (jRBSuperior.isSelected()){
                infoJanela.put("Semestre", String.valueOf(jCBSemestre.getSelectedIndex()));
                infoJanela.put("Curso", String.valueOf(jCBCurso.getSelectedItem()));
            }
            
            infoJanela.put("Horario", jTFCargaHoraria.getText());
            infoJanela.put("Id_Professor", jTFCodigoProfessor.getText());
            
            ControlDisciplina cd = new ControlDisciplina();
            
            if (!jTFCodigo.getText().isEmpty()){
                this.infoJanela.put("Codigo", jTFCodigo.getText());
                
                if (cd.ControlAlterar(this.infoJanela)){
                    JOptionPane.showMessageDialog(null, "Alteração realizada com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Não foi possível salvar as alterações!");
                }
            }else{
               if (cd.ControlInserir(this.infoJanela)) {
                    JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Não foi possível realizar o cadastro!");
                } 
            }
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios (em negrito)!");
        }
    }//GEN-LAST:event_jBtnSalvarActionPerformed

