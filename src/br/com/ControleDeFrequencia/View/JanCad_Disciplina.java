    @Override
    public void setValoresSelecao(HashMap selecao) {
        jTFCodigo.setText(selecao.get("Codigo").toString());
        jTFDisciplina.setText(selecao.get("Disciplina").toString());
        jTFCargaHoraria.setText(selecao.get("Carga_Horaria").toString());
        jTFCodigoProfessor.setText(selecao.get("Codigo_Professor").toString());
        jCBProfessor.setSelectedItem(selecao.get("Nome_Professor").toString());
        
        jCBSemestre.setSelectedIndex((int) selecao.get("Semestre"));
        jCBSemestre.setEnabled(jCBSemestre.getSelectedIndex() > 0);
        jRBSuperior.setSelected(jCBSemestre.getSelectedIndex() > 0);
        jCBCurso.setEnabled(jCBSemestre.getSelectedIndex() > 0);
        jCBCurso.setSelectedItem(selecao.get("Curso").toString());
        
        jCBSerie.setSelectedIndex((int) selecao.get("Serie"));
        jCBSerie.setEnabled(jCBSerie.getSelectedIndex() > 0);
        jRBMedio.setSelected(jCBSerie.getSelectedIndex() > 0);
    }
    
    public void preencherListaProfessores(){
        this.professores = new ControlProfessor().pegarListaDeProfessores();
        
        for (String[]  professor: professores) {
            jCBProfessor.addItem(professor[1]);
        }
    }
    
